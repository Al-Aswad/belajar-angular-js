          data: {
            kode: kode
          },
          success: function(data) {
            // alert(data);
            $('.preload').removeClass('active');
            data = JSON.parse(data)
            // hrgjual
            console.log(data);
            console.log(data.status);
            if (data.status == "success") {
              let nama = data.data[0].nm_part;
              let harga = data.data[0].hrgjual

              document.getElementById("hargaSperpart1").value = harga
              harga = new Intl.NumberFormat(['ban', 'id']).format(harga)
              document.getElementById("namaSperpart").value = nama;
              document.getElementById("hargaSperpart").value = harga;
            } else {
              alert(data.status + ' ' + data.message);
              document.getElementById("namaSperpart").value = "";
              document.getElementById("hargaSperpart").value = "";
            }
          }
        });

      } else {
        alert('Nomor Sperpart Harus di isi !');
      }
    });
  });
